 # 1. NUMERIC TYPES (Numbers ke liye)
# int       -> Poore numbers (e.g., x = 10)
# float     -> Point wale numbers (e.g., x = 10.5)
# complex   -> Real aur imaginary numbers (e.g., x = 2 + 3j)

# 2. SEQUENCE TYPES (Data ko ek line/order mein rakhne ke liye)
# str       -> Text yaani strings (e.g., x = "Hello")
# list      -> Change hone wali list (e.g., x = [1, 2, 3])
# tuple     -> Change NA hone wali list (e.g., x = (1, 2, 3))

# 3. MAPPING TYPE (Key aur Value ke joray)
# dict      -> Dictionary, data save karne ke liye (e.g., x = {"name": "Sehar"})

# 4. SET TYPES (Unique cheezon ke liye)
# set       -> Bina order ke unique items (e.g., x = {1, 2, 3})
# frozenset -> Set jo badla nahi ja sakta (e.g., x = frozenset({1, 2}))

# 5. BOOLEAN TYPE (Haan ya Na)
# bool      -> Sirf True ya False (e.g., x = True)

# 6. BINARY TYPES (Bytes data ke liye)
# bytes     -> Byte data (e.g., x = b"Hello")
# bytearray -> Badalne wala byte data (e.g., x = bytearray(5))
# memoryview-> Memory ko access karne ke liye (e.g., x = memoryview(b"A"))

# 7. NONE TYPE (Khaali)
# NoneType  -> Jab variable mein kuch na ho (e.g., x = None)
